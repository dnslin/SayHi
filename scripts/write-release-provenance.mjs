import { execFileSync } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const revision = execFileSync("git", ["rev-parse", "HEAD"], {
  cwd: repositoryRoot,
  encoding: "utf8",
}).trim();
if (!/^[0-9a-f]{40}$/u.test(revision)) {
  throw new Error("Git did not return a full source revision.");
}

const output = resolve(
  repositoryRoot,
  "packages/core/src/release-provenance.generated.ts",
);
await mkdir(dirname(output), { recursive: true });
await writeFile(
  output,
  [
    "// Generated by scripts/write-release-provenance.mjs; do not edit.",
    "export const RELEASE_SOURCE_PROVENANCE = Object.freeze({",
    '  repository: "https://github.com/dnslin/SayHi",',
    `  revision: "git:${revision}",`,
    "});",
    "",
  ].join("\n"),
  "utf8",
);
